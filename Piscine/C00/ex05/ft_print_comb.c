#include<unistd.h>

void	ft_print_comb(void)
{
	char	a[3];

	a[0] = '0';
	a[1] = '0';
	a[2] = '0';
	while (a[0] <= '9' )
	{
		a[1] = a[0] + 1;
		while (a[1] <= '9' )
		{
			a[2] = a[1] + 1;
			while (a[2] <= '9' )
			{
				write(1, a, 3);
				if (a[0] != '7' || a[1] != '8' || a[2] != '9')
					write(1, ", ", 2);
				a[2]++;
			}
			a[1]++;
		}
		a[0]++;
	}
}
