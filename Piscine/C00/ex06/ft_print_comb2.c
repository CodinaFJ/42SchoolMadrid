#include<unistd.h>

void	ft_print_comb2(void)
{
	int		n[2];
	char	c[5];

	n[0] = 0;
	n[1] = 0;
	c[2] = ' ';
	while (n[0] < 99)
	{
		n[1] = n[0] + 1;
		while (n[1] < 100)
		{
			c[0] = '0' + n[0] / 10;
			c[1] = '0' + n[0] % 10;
			c[3] = '0' + n[1] / 10;
			c[4] = '0' + n[1] % 10;
			write(1, c, 5);
			if (n[0] != 98 || n[1] != 99)
				write(1, ", ", 2);
			n[1]++;
		}
		n[0]++;
	}
}
